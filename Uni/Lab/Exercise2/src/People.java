public class People {
    private String name;
    private String date;

    public String getName()
    {
        return this.name;
    }

    public void setName(String fname)
    {
        this.name = fname;
    }

    public String getDate()
    {
        return this.date;
    }

    public void setDate(String date1)
    {
        this.date = date1;
    }
}
